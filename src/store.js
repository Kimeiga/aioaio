function generateRandomSyllable() {
	// List of all possible vowels (including diacritics)
	var vowels = ['a', 'e', 'i', 'o', 'u'];
	// List of all possible consonants (including diacritics)
	var consonants = ['b', 'c', 'd', 'f', 'g', 'h', 'j', 'k', 'l', 'm', 'n', 'p', 'q', 'r', 's', 't', 'v', 'w', 'x', 'z'];

	// Determine if the syllable starts with a consonant
	var startsWithConsonant = Math.random() >= 0.5;

	// Choose a random vowel
	var vowel = vowels[Math.floor(Math.random() * vowels.length)];

	if (startsWithConsonant) {
		// Choose a random consonant
		var consonant = consonants[Math.floor(Math.random() * consonants.length)];
		return consonant + vowel;
	} else {
		return vowel;
	}
}

function generateRandomString() {
	// Randomly determine the number of syllables (between 2 and 5)
	var numSyllables = Math.floor(Math.random() * 4) + 2;

	// Generate the syllables and concatenate them into a string
	var result = '';
	for (var i = 0; i < numSyllables; i++) {
		result += generateRandomSyllable();
	}

	return result;
}

